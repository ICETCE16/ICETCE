<h3 style="color:red">ABOUT THE INSTITUTE</h3>
<p>Established in the year 1976,<p style="color:red"> Kamla Nehru Institute of Technology,
Sultanpur (Near Ayodhya,Uttar Pradesh)</p>is one of the leading technical
institutions of the region and is responsible for producing top-grade
engineers with skill sets comparable with the best in the world. It also
renders the testing and consultancy services to the neighboring industries
and various other agencies. This is presently an autonomous government
engineering institute (under 2f and 12B of UGC Act) and affiliated to Dr.
APJ Abdul Kalam Technical University, Lucknow, India. The institute
offers B.Tech. and M. Tech. courses in Civil, Electrical, Mechanical,
Electronics, Computer Science & Engineering and Information
Technology disciplines in addition to M.C.A. course as well.<br>
</p>
